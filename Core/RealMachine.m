% RealMachine
% Defines the base class for machines. Assumes a double machine for
% accommodating some cluster approaches which have the user do all file ops
% on one machine and all job submissions on another.  Machines without the
% dual structure just use the same info for both.  Provides basic command
% execution and file existence checking on the machine.
classdef RealMachine < handle
    properties
        id; % A unique string identifying the combination machine
        % A string identifying the machine from the communications point of
        % view - for example, all queues of a cluster will have the cluster
        % comms id.
        commsID;
%         configID;

        fsUserName;     % File system machine username
        fsPassword;     % File system machine password
        fsIPAddress;    % File system machine ipaddress
        fsUseDualKey;   % Use dual key authentication instead of password

        jsUserName;     % Job submission machine username
        jsPassword;     % Job submission machine password
        jsIPAddress;    % Job submission machine ipaddress
        jsUseDualKey;   % Use dual key authentication instead of password

        simAuth;        % Dual key data
        
        machineDataFilename;

        % The OS of this specific machine from OSType class
        osType; 
        
        fsConnection;  % File system machine ssh connection
        jsConnection;  % Job submission machine ssh connection

        % Communicate or transfer files without SSH if host = this machine
        hostID;  
    end
    
    methods
        % ----------
        % Constructor
        function obj = RealMachine(md, hostID, auth)
            obj.id           = md.getSetting('id');
            obj.commsID      = md.getSetting('commsID');
            obj.fsUserName   = md.getSetting('fsUserName');
            obj.fsPassword   = md.getSetting('fsPassword');
            obj.fsIPAddress  = md.getSetting('fsIPAddress');
            obj.jsUserName   = md.getSetting('jsUserName');
            obj.jsPassword   = md.getSetting('jsPassword');
            obj.jsIPAddress  = md.getSetting('jsIPAddress');
            % Basic assumption is that all targets are unix.
            obj.osType       = OSType.UNIX; 

            obj.simAuth = auth;
            obj.hostID  = hostID;
            obj.machineDataFilename = [md.getSetting('id') 'Data.dat'];
            
            % Unless is a single machine setup, we check validity of
            % password/dual key settings and set up connections to the fs
            % and js machines
            if ~obj.simAuth.isSingleMachine()
                % See simAuthenticate.m for policy summary
                if obj.simAuth.isUseDualKey()
                    if isempty(obj.fsPassword)
                        obj.fsUseDualKey = true;
                    else
                        obj.fsUseDualKey = false;
                    end
                else
                    if isempty(obj.fsPassword)
                        error(['Authentication Error: '...
                            'fsPassword for machine ' obj.id ' is empty but '...
                            'Password authentication was selected.']);
                    end
                    obj.fsUseDualKey = false;
                end

                if obj.simAuth.isUseDualKey()
                    if isempty(obj.jsPassword)
                        obj.jsUseDualKey = true;
                    else
                        obj.jsUseDualKey = false;
                    end
                else
                    if isempty(obj.jsPassword)
                        error(['Authentication Error: '...
                            'jsPassword for machine ' obj.id ' is empty but '...
                            'Password authentication was selected.']);
                    end
                    obj.jsUseDualKey = false;
                end

                if obj.fsUseDualKey
                    obj.fsConnection =...
                        ssh2_config_publickey(obj.fsIPAddress, ...
                                obj.fsUserName, obj.simAuth.getKeyFile(),...
                                obj.simAuth.getKeyPassphrase());
                else
                    obj.fsConnection =...
                        ssh2_config(obj.fsIPAddress,...
                                    obj.fsUserName, obj.fsPassword);
                end
                % Don't ignore responses is the NeuroManager default
                obj.fsConnection.command_ignore_response = 0;
                obj.fsConnection.autoreconnect = 1;  % TRIAL ONLY

                if obj.jsUseDualKey
                    obj.jsConnection =...
                        ssh2_config_publickey(obj.jsIPAddress, ...
                                obj.jsUserName, obj.simAuth.getKeyFile(),...
                                obj.simAuth.getKeyPassphrase());
                else
                    obj.jsConnection =...
                        ssh2_config(obj.jsIPAddress,...
                                    obj.jsUserName, obj.jsPassword);
                end

                % Don't ignore responses is the NeuroManager default
                obj.jsConnection.command_ignore_response = 0;
                obj.jsConnection.autoreconnect = 1;  % TRIAL ONLY
            end
        end
        
        % ----------------
        % We store the connection in the class properties; there seems to
        % be no issue with connections timing out; possibly because the
        % software is polling all machines every minute or less.  So we are
        % ignoring connection timeout for now.
        function result = issueMachineCommand(obj, command, commandtype)
        % Execute a command on this machine and get the result
            % If this machine is also the host, then just do it (it doesn't
            % seem right to do ssh to the same machine), but we have to 
            % match the result format of the ssh library. We can't use the
            % strsplit command since it is not in the 2012 version of MATLAB.
            % In actuality the ssh commands throw away the status; if you
            % want to get it you have to do an "echo $?". We copy this
            % behavior here so the behavior is the same for both.
            if strcmp(obj.id, obj.hostID)
                [~, cmdout] = system(command);
                if isempty(cmdout)
                    cmdsplit = {''};
                else
                    c = textscan(cmdout, '%s', 'delimiter', sprintf('\n'));
                    cmdsplit = c{1,1};
                end
%                 i=1; cmdsplit = cell(1);
%                 while true
%                     [str, cmdout] = strtok(cmdout, char(10)); %#ok<STTOK>
%                     if isempty(str), break; end
%                     cmdsplit{i} = strtrim(str);
%                     i = i+1;
%                 end
                result = cmdsplit';
            else
                % If remote:
                switch commandtype
                    case CommandType.FILESYSTEM
                        [obj.fsConnection, result] =...
                            ssh2_command(obj.fsConnection, command); 
                    case CommandType.JOBSUBMISSION
                        [obj.jsConnection, result] =...
                            ssh2_command(obj.jsConnection, command); 
                    otherwise % default is filesystem machine
                        [obj.fsConnection, result] =...
                            ssh2_command(obj.fsConnection, command); 
                end
            end
        end
        
        % ----------------
        function issueMachineCommandDontWait(obj, command, commandtype)
        % Execute a command on this machine but don't wait for the result
            % If this is also the host then just do it.  
            if strcmp(obj.id, obj.hostID)
                system([command ' &']); % Note no return value: compare IssueMachineCommand
            else
                switch commandtype
                    case CommandType.FILESYSTEM
                        obj.fsConnection.command_ignore_response = 1;
                            [obj.fsConnection, ~] =...
                                ssh2_command(obj.fsConnection, command); 
                        obj.fsConnection.command_ignore_response = 0;
                    case CommandType.JOBSUBMISSION
                        obj.jsConnection.command_ignore_response = 1;
                            [obj.jsConnection, ~] =...
                                ssh2_command(obj.jsConnection, command); 
                        obj.jsConnection.command_ignore_response = 0;
                    otherwise
                        obj.fsConnection.command_ignore_response = 1;
                            [obj.fsConnection, ~] =...
                                ssh2_command(obj.fsConnection, command); 
                        obj.fsConnection.command_ignore_response = 0;
                end
            end
        end
        
        % ----------------
        function result = checkForCheckfile(obj, checkfilePath)
        % Returns true if the file exists; assumes filesystem machine
            command = ['test -e ' path2UNIX(checkfilePath) '; echo $?;'];
            machineResult = obj.issueMachineCommand(command, CommandType.FILESYSTEM); 
            if strcmp(machineResult{1}, '0')
                result = true;
            else
                result = false;
            end
        end
        
        % ----------------
        % Pretty intense communications so use sparingly and with v small
        % lists.
        function result = checkForCheckfileList(obj, checkfilePathlist)
        % Returns a boolean array with true for each file that exists;
        % assumes filesystem machine 
            for i = 1:length(checkfilePathlist)
                command = ['test -e ' path2UNIX(checkfilePathlist{i}) '; echo $?;'];
                machineResult = obj.issueMachineCommand(command, CommandType.FILESYSTEM); 
                if strcmp(machineResult{1}, '0')
                    result(i) = true; %#ok<AGROW>
                else
                    result(i) = false; %#ok<AGROW>
                end
            end
        end
        
        % ----------------
        function result = checkForDirectory(obj, directory)
        % Returns true if the directory exists; assumes filesystem machine
            command = ['test -d ' path2UNIX(directory) '; echo $?;'];
            machineResult = obj.issueMachineCommand(command, CommandType.FILESYSTEM); 
            if strcmp(machineResult{1}, '0')
                result = true;
            else
                result = false;
            end
        end
        
        % ----------------
        % Checks for a file to appear on remote machine at the indicated
        % path and returns when it appears. LATER ADD A TIMEOUT AND
        % CONDITIONAL RETURN OF INFO.  
        function waitForCheckfile(obj, checkfilePath, delay)
        % Returns true when the file exists; assumes filesystem machine
        % Waits delay between checks
            while true
                if obj.checkForCheckfile(checkfilePath)
                    break;
                end
                pause(delay);
            end
        end
        
        % ----------------
        function id = getID(obj)
            id = obj.id;
        end
        
        % ----------------
        function os = getOS(obj)
            os = obj.osType;
        end
        
        % ---------------
        function str = getAgentInfo(obj)
            str = obj.simAuth.getAgentInfo();
        end
        
        % ---------------
        function vstr = getVersionString(obj, commandtype)
            command = 'uname -a';
            result = obj.issueMachineCommand(command, commandtype);
            vstr = result{1};
        end
        
        % We assume that the remote cluster is well time-synced and just
        % query the jobsubmission machine
        function time = getMachineTime(obj)
            command = 'date +"%d-%b-%Y %H:%M:%S"';
            result = obj.issueMachineCommand(command, CommandType.JOBSUBMISSION);
            timestr = result{1};
            time = datetime(timestr, 'InputFormat', 'dd-MMM-yyyy HH:mm:ss');
        end
       
        % ----------------
        function delete(obj)
            if ~strcmp(obj.id, obj.hostID)
                ssh2_close(obj.fsConnection); 
                ssh2_close(obj.jsConnection); 
            end
        end
    end 
end
