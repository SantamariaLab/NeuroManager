% SimSet
% Defines the SimSet class. A SimSet, loosely, is a set of simulation input
% parameter sets / vectors.
classdef SimSet < handle
    properties
        % ID for the SimSet
        id;

        % The NeuroManager RESULTS+DateTime baseDir
        nmResultsDir; 
        
        % Full pathname of the SimSet's working directory on the host
        % Created in constructor from nmResultsDir and the SimSetID 
        baseDir;
        
        % Each SimSet has a separate final simset report
        % POSSIBLY MOVE SIMSETREPORT INTO THE SIMSET CLASS
        finalReport;
        
        % All Simulations in a SimSet have same Type from SimType class
        type;  
        
        % The set of simulations created using the sim specification
        sims;
        
        % Version requirements? PUT THIS OFF A BIT
        % (not implemented yet)
        
        % The sim specification file full pathname 
        source;
        
        % The number of simspec lines pulled in from the spec file
        % Used only for temporary ease of implementation
        totalNumSims;

        % The maximum number of parameters on a simspec line
        % This location will change later to support variable numbers
        % defined probably in the Simulator.
        MAX_PARAMS = 10;    

        % Handle supplied by NeuroManager for making log entries.
        log;
        
        % Handle supplied by NeuroManager for making notifications.
        notificationSet;
    end
    
    properties (Access=private)
        % Path of the report; automatically generated
        reportPathname = ''; 
        
        % Handle here to avoid multiple open/close
        reportFileHandle;    
    end
    
    methods
        function obj = SimSet(source, simspec, resultsBasedir,...
                                      log, notificationSet)
        % If source is empty, uses the simspec; otherwise uses the source
        % file. This approach is clumsy but MATLAB doesn't support
        % overloading by signature.
            % SimSet Constructor
            if nargin == 0
                obj.id = '';
                obj.nmResultsDir = '';
                obj.baseDir = '';
                obj.type = SimType.UNASSIGNED;
                obj.sims = Simulation.empty(0);
                obj.totalNumSims = 0;
                % ignore remainder for now
            else
                % Test inputs for validity
                % (not implemented yet)
                obj.source = source;
                obj.nmResultsDir = resultsBasedir;
                obj.totalNumSims = 0; % TEMPORARY
                obj.log = log;
                obj.notificationSet = notificationSet;
                obj.sims = Simulation.empty(0);

                % Import and Process the SimSetSpec
                if ~isempty(source)
                    simspecTemp = obj.importSimSpec(obj.source); 
                else
                    simspecTemp = simspec;
                end
                [result, errmsg] = obj.processSimSpec(simspecTemp);
                if result
                    ME = MException('ClassdefSimSet:SimSpecProcessFailure', ...
                                    ['Problem processing SimSpec file ' ...
                                    strrep(obj.source, '\', '\\') ...
                                    '. Incorrect token ' ...
                                    errmsg '. Program aborting.']);
                    throw(ME);
                end

                % Copy the SimSpec into the SimSet directory for snapshot
                % documentation. Add to name and make read-only to prevent
                % future editing.
                if (~isempty(obj.source))
                    [~, name, ext] = fileparts(obj.source);
                    newSource = [obj.baseDir name '_COPY_DO_NOT_EDIT' ext];
                    [result, errmsg] = copyfile(obj.source, newSource);
                    fileattrib(newSource, '-w');
                    % Handle bad copies here 
                    %  (not implemented yet)
                    if result
                        obj.log.write([obj.source ...
                            ' copied to SimSet Base Directory as '...
                            [name ext] '.']);
                    else
                        % Policy is to fail easily before simulations begin to
                        % ensure proper setup and provenance.
                        obj.log.write(['ERROR: Could not copy '...
                                    obj.source ' to SimSet Base Directory.']);
                        error(errmsg);
                    end
                else
                    % No input file so for documentation we have to print the
                    % simset  
                    filename = [obj.id, '.txt'];
                    dir = obj.baseDir;
                    printpath = fullfile(dir, filename);
                    simspec.print(printpath);
                    obj.log.write(['SimSpec printed to SimSet Base Directory as ' ...
                                                                filename '.']);
                end
            end
        end
        
        
        % ----------------
        function sim = checkOutSimulation(obj)
            % Check out the first unrun simulation encountered; set its
            % status to INPROGRESS while it is checked out.
            for i=1:obj.totalNumSims
                if(obj.sims(i).updateState() == SimulationState.UNRUN)
                    sim = obj.sims(i);
                    sim.setState(SimulationState.INPROGRESS);
                    return;
                end
            end
            % none found (might use HasUNRUN() to prevent this)
            sim = 0;
        end
        
        % ----------------
        function returnSimulation(obj, sim, state) %#ok<INUSL>
            % Checks the Simulation back in with the new state
            % Caller (NeuroManager) is finished with the sim, at least for now.
            % If it could not get a machine for the simulation, it wants to 
            % put it back as UNRUN.  If the sim failed, it will set
            % the status as FAILED.  If the sim succeeded, it will set the
            % status as COMPLETE. 

            % Check status for validity here
            % (not implemented yet)
            sim.setState(state);
        end
        
        % ----------------
        function result = hasUNRUN(obj)
        % Checks if any sim is UNRUN and thus available for expression on a Simulator.
            result = false;
            for i=1:obj.totalNumSims
                if (obj.sims(i).updateState() == SimulationState.UNRUN)
                    result = true;
                    break;
                end
            end
        end
        
        % ----------------
        function result = isFullyProcessed(obj)
            % Boolean: Have all the simulations in SimSet been fully processed?
            result = true;
            for i=1:obj.totalNumSims
                if ((obj.sims(i).state == SimulationState.UNRUN)||...
                    (obj.sims(i).state == SimulationState.INPROGRESS)||...
                    (obj.sims(i).state == SimulationState.SUBMITTED)||...
                    (obj.sims(i).state == SimulationState.RUNNING)||...
                    (obj.sims(i).state == SimulationState.RUNCOMPLETE)||...
                    (obj.sims(i).state == SimulationState.READYFORDOWNLOAD)||...
                    (obj.sims(i).state == SimulationState.DOWNLOADINPROGRESS))
                    result = false;
                    return;
                end
            end
        end
        
        % ----------------
        function setID(obj, id)
            % Sets the SimSet ID and constructs the SimSet directory on host,
            % which is a little non-transparent.  Requires the
            % log to have been set up previously.
            obj.id = id;
            obj.log.write(['SimSet: SimSet ' obj.id ' started.']);
           
            obj.baseDir = fullfile(obj.nmResultsDir, obj.id);
            [status, ~, messid] = mkdir(obj.baseDir);
            if ~status
                ME = MException('ClassdefSimSet:SimSetbaseDirMakeFailure', ...
                                ['SimSet: Could not create SimSet baseDirectory '...
                                 obj.baseDir '.']);
                throw(ME);
            end
            
            % Note: the following just logs the note and keeps going.
            if strcmp(messid, 'MATLAB:MKDIR:baseDirectoryExists')
                obj.log.write(['Simulation Manager: '...
                    'Note: SimSet baseDirectory already existed at start!']);
            else
                obj.log.write(['SimSet baseDirectory created: ' obj.baseDir]);
            end
        end
        
        % ----------------
        function idstring = getID(obj)
            idstring = obj.id;
        end
        
        % ----------------
        function type = getType(obj)
            type = obj.type;
        end
        
        function dir = getBaseDir(obj)
            dir = obj.baseDir;
        end
        
        % ----------------
        function setType(obj, type)
            % Sets the SimSet type.
            % Note: this does not set the type of any sims (just the set),
            % so use it before adding any sims.

            % Check for valid type from class SimType
            % (not implemented yet)
            obj.type = type;
        end
        
        % ----------------
        function addSimDef(obj, id, type, notify, params)
            % Constructs a Simulation from input params and adds it to the
            % SimSet's list. params is a cell array of strings
            obj.totalNumSims = obj.totalNumSims + 1;
            if isempty(id)
                ME = MException('ClassdefSimSet:EmptySimDefID', ...
                                'SimSet: SimDef ID must not be empty.');
                throw(ME);
            end
            newSim = Simulation(obj.baseDir, id, type, notify, params);
            obj.sims(obj.totalNumSims) = newSim;
        end
        
        function num = getTotNumSims(obj)
            num = obj.totalNumSims;
        end
        
        % ----------------
        function results = wrapup(obj)
            % Wraps up the processing of the SimSet,
            % including writing the Final Report and doing last notification.
            
            % Copy matlab figures into the figures directory HARDWIRED
            % NOT IMPLEMENTED YET - SEE 
            % http://mooring.ucsd.edu/software/matlab/doc/toolbox/diskfun/cp2find.html
            % (not implemented yet)
            
            % Create the results struct array
            for i=1:obj.totalNumSims
                results.basedir = obj.baseDir;
                % The ID of the Simulation
                results.simulations(i).simid = obj.sims(i).getID();
                % The full pathname on the host where the Simulation
                % results are stored
                results.simulations(i).location = obj.sims(i).getHostBaseDir();
                % Simulation outcome: COMPLETE or FAILED
                results.simulations(i).result = obj.sims(i).getResult();
                % The time the Simulator was given the Simulation
                results.simulations(i).handofftime = obj.sims(i).getHandoffTime();
                % The time the job was submitted on the remote
                results.simulations(i).submissiontime = obj.sims(i).getSubmissionTime();
                % The time on the remote the job actually started running
                results.simulations(i).runstarttime = obj.sims(i).getRunStartTime();
                % The time on the remote the job finished
                results.simulations(i).runcompletetime = obj.sims(i).getRunCompleteTime();
                % The time on the host that the Simulation was finished up
                results.simulations(i).simfullproctime = obj.sims(i).getSimFullProcTime();
                % The tic-toc time obtained by RunSimulation() on the
                % remote
                results.simulations(i).executiontime = ...
                    sprintf('%10.2f', obj.sims(i).getExecutionTime());
                % The SimMachine the Simulation ran on 
                results.simulations(i).machineid = obj.sims(i).getMachineID();
                % The Simulator the Simulation ran on 
                results.simulations(i).simulatorid = obj.sims(i).getSimulatorID();
            end
            
            % Final Report
            % For now, we just list the runtimes.
            reportfile = fullfile(obj.baseDir, [obj.id '_Report.txt']);
            obj.finalReportStart(reportfile);
            obj.finalReportWrite([obj.id ' Report']);
            obj.finalReportWrite(datestr(now));
            lines = repmat(sprintf('-'), 1, 120);
            obj.finalReportWrite(lines);
            obj.finalReportWrite('Simulation times (all times in seconds)');
            %
            % 20 12 10 20 30 20
            obj.finalReportWrite(sprintf('\t%s%s%s%s%s%s',...
                                  'SimID               ',...
                                  'Time        ',...
                                  'Result    ',...
                                  'Simulator           ',...
                                  'Machine                       ',...
                                  'ML Version'));
            total = 0;
            validsims = 0;
            max = 0.0;
            min = Inf;
            for i=1:obj.totalNumSims
                simtime = obj.sims(i).getExecutionTime();
                switch obj.sims(i).getResult()
                    case 'FAILED'
                        resultStr = 'Failed';
                    case 'COMPLETE'
                        resultStr = 'Success';
                    otherwise
                        resultStr = 'Unknown';
                end
                if (simtime >= 0.0)
                    obj.id = obj.sims(i).getID();
                    simid = obj.sims(i).getSimulatorID();
                    machineid = obj.sims(i).getMachineID();
                    mlversion = obj.sims(i).getMLVersion();
                    entry = sprintf('\t%s%s%10.2f  %s%s%s%s%s%s%s',...
                        obj.id, repmat(sprintf(' '), 1, 20-length(obj.id)),...
                        simtime, ...
                        resultStr,...
                        repmat(sprintf(' '), 1, 10-length(resultStr)),...
                        simid, repmat(sprintf(' '), 1, 20-length(simid)),...
                        machineid,...
                        repmat(sprintf(' '), 1, 30-length(machineid)),...
                        mlversion);
                    obj.finalReportWrite(entry);
                    total = total + simtime;
                    validsims = validsims + 1;
                    if (simtime > max) max = simtime; end %#ok<SEPEX,*SEPEX>
                    if (simtime < min) min = simtime; end %#ok<SEPEX>
                else
                    entry = sprintf('\t%s\t%s',...
                                    obj.sims(i).GetID(), '     -----');
                    obj.finalReportWrite(entry);
                end
            end
            obj.finalReportWrite(lines);
            entry = sprintf('%s% 10.2f', 'Total time:            ', total);
            obj.finalReportWrite(entry);
            entry = sprintf('%s% 10.2f', 'Min time:              ', min);
            obj.finalReportWrite(entry);
            entry = sprintf('%s% 10.2f', 'Max time:              ', max);
            obj.finalReportWrite(entry);
            entry = sprintf('%s% 10.2f', 'Avg time (valid sims): ',...
                                                        total/validsims);
            obj.finalReportWrite(entry);
            entry = sprintf('%s', lines);
            obj.finalReportWrite(entry);
            obj.finalReportClose();

            obj.log.write([obj.id ' Report written to ' reportfile]);
            
            % SimSet signoff
            notificationSubject = ['Re: NeuroManager Notice'];
            obj.log.write([obj.id ' SimSet finished.']);
            if obj.notificationSet.isEnabled()
                obj.notificationSet.send(notificationSubject,...
                                         'SimSet finished.', '');
            end
        end
    end
    
    methods (Access=private)
        % ----------------
        function finalReportStart(obj, fullPathname)
            obj.reportFileHandle = fopen(fullPathname, 'w');
            if (obj.reportFileHandle == -1)
                ME = MException('SimSetReport:ReportFileFailure', ...
                                ['NeuroManager: Could not create '...
                                 fullPathname]);
                throw(ME);
            end
            obj.reportPathname = fullPathname;
        end
        
        % ----------------
        function finalReportWrite(obj, msg)
            % Writes the message to the report
            fprintf(obj.reportFileHandle, '%s\n', msg);
        end
        
        % ----------------
        function finalReportClose(obj)
            % Closes out the report
            obj.finalReportWrite(['******** NeuroManager Programming '...
                                  'by Dave Stockton ********']);
            fclose(obj.reportFileHandle);
        end
    end 
