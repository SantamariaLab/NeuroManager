% displayStatusWebPage.m
% Displays an in-MATLAB real-time-ish display of the status of all
% simulations.  Does not work outside of MATLAB at this time.
% We avoid using a style sheet for now.
% stage can be 'initial', 'testmachine', 'constructmachine', 'SimSet', or
% 'final'.
function displayStatusWebPage(obj, stage)
    filename = 'SimSetStatus.html';
    filepath = fullfile(obj.simResultsDir, filename);
    f = fopen(filepath, 'w');
    % If the file open fails, just don't do the page and move on
    if f == -1
        obj.log.write(['Could not open webpage file '...
                       filepath ...
                       'for writing; continuing operations.']);
    else
        fprintf(f, '%s\n', ['<!DOCTYPE html>']);
        fprintf(f, '%s\n', ['<html>']); 
        fprintf(f, '%s\n', ['<head>']);
        switch stage
            case {'initial', 'testmachine', 'constructmachine'}
                fprintf(f, '%s\n', ['<title>NeuroManager ' obj.getVersion() '</title>']); 
            otherwise
                fprintf(f, '%s\n', ['<title>SimSet ' obj.nmSimSet.getID() ' Status Page</title>']); 
        end
        fprintf(f, '%s\n', ['</head>']); 
        fprintf(f, '%s\n', ['<body>']); 
        fprintf(f, '%s\n', ['<table style="width:100%">']); 
        if strcmp(stage,'final')
                if obj.hostMachineData.osType == OSType.WINDOWS
                    fprintf(f, '%s\n', ['<tr> <td title="Welcome to NeuroManager!"><b>NeuroManager ' obj.getVersion() '</b></td> '...
                            '<td align="center"><b><a href="matlab:winopen(''' obj.getSimResultsDir() ''');"'...
                            ' style="color:#2b65ec" title="Click to open Results Directory">'...
                            'Results Directory</a></b></td>'...
                            '<td align="right"><b><a href="matlab:dos([''start '' ''http://www.utsa.edu/Santamarialab/'']);"'...
                            ' style="color:#2b65ec" title="Click to open Santamaria Lab Website">Santamaria Lab</a></b></td></tr>'...
                            ]); 
                else
                fprintf(f, '%s\n', ['<tr> <td title="Welcome to NeuroManager!"><b>NeuroManager ' obj.getVersion() '</b></td> '...
                            '<td align="center"><b><a href="matlab:cd ' obj.localMachineDir ';OpenMyUNIXDirectory(''' path2UNIX(obj.getSimResultsDir()) ''');"'...
                            ' style="color:#2b65ec" title="Click to open Results Directory">'...
                            'Results Directory</a></b></td>'...
                            '<td align="right"><b><a href="matlab:cd ' obj.localMachineDir ';OpenMyUNIXWebsite(''www.utsa.edu/Santamarialab'');"'...
                            ' style="color:#2b65ec" title="Click to open Santamaria Lab Website">Santamaria Lab</a></b></td></tr>'...
                            ]); 
                end
        else
            fprintf(f, '%s\n', ['<tr> <td title="Welcome to NeuroManager!"><b>NeuroManager ' obj.getVersion() '</b></td> '...
                        '<td align="center" style="color:#2b65ec" title="clickable link will appear when run is fully complete"><b>'...
                        'Results Directory</b></td>'...
                        '<td align="right" style="color:#2b65ec" title="clickable link will appear when run is fully complete"><b>'...
                        'Santamaria Lab</b></td></tr>']); 
        end
        fprintf(f, '%s\n', ['<table>']); 
        switch stage
            case 'initial'
                fprintf(f, '%s\n', ['<p>Doing initial processing.</p>']); 
            case 'testmachine'
                fprintf(f, '%s\n', ['<p>Testing machine communications.</p>']); 
            case 'constructmachine'
                fprintf(f, '%s\n', ['<p>Constructing machine set; please wait... '...
                                    '<img src="file:///' fullfile(obj.simCoreDir, 'Artwork_DesignService_fedora-spinner.gif') '"'...
                                    ' alt="NeuroManager is working..."'...
                                    ' title="Spinner Image from Artwork_DesignService"'...
                                    ' align="middle"'...
                                    ' width="50" height="50"/></p>']); 
            case {'SimSet', 'final'}
%                 fprintf(f, '%s\n', ['<p><b>Calling script or function:</b> ' obj.callingFunctionData(2,1).file]);
                if ~isempty(obj.simSpecFullPath)
                    fprintf(f, '%s\n', ['<br><b>Input SimSpec file:</b> ' obj.simSpecFullPath]);
                else
                    fprintf(f, '%s\n', ['<br>Input SimSpec constructed on the fly']);
                end
                fprintf(f, '%s %u%s\n', '<br><b>Size of Simulator Pool:</b>', obj.numSimulators, ' </p>');
                fprintf(f, '%s\n', ['<p>SimSet "' obj.nmSimSet.getID() '" status '... 
                                    'at ' obj.getSnapshotTimeStr() ' </p>']); 
                fprintf(f, '%s\n', ['<table cellspacing="15">']); 
                fprintf(f, '%s\n', ['<tr> <td><b>SIMID</b></td> '...
                                         '<td><b>State</b></td> '...
                                         '<td><b>Submission Time</b></td> '...
                                         '<td><b>Run Start Time</b></td> '...
                                         '<td align="right"><b>Job ID</b></td> '...
                                         '<td><b>Simulator</b></td> '...
                                         '<td><b>Machine</b></td>'...
                                         '<td><b>Result</b></td></tr>'...
                                         ]); 
                for i=1:obj.nmSimSet.getTotNumSims()
                    simulation = obj.nmSimSet.sims(i);
                    id = simulation.getID();
                    resultStr = '?';
                    switch simulation.getState()
                        case SimulationState.UNRUN
                            statestr = 'UNRUN';
                        case SimulationState.INPROGRESS
                            statestr = 'INPROGRESS';
                        case SimulationState.SUBMITTED
                            statestr = 'SUBMITTED';
                        case SimulationState.RUNNING
                            statestr = 'RUNNING';
                        case SimulationState.RUNCOMPLETE
                            statestr = 'RUNCOMPLETE';
                        case SimulationState.READYFORDOWNLOAD
                            statestr = 'READYFORDOWNLOAD';
                        case SimulationState.DOWNLOADINPROGRESS
                            statestr = 'DOWNLOADING';
                        case SimulationState.FULLYPROCESSED
                            statestr = '<font color="#4cc552">FULLYPROCESSED</font>';
                            [result, ~, ~, ~] = simulation.getResults();
                            if strcmp(result, 'COMPLETE')
                                resultStr = 'Success';
                            elseif strcmp(result, 'FAILED') 
                                resultStr = '<font color="#f70dia">Failed</font>';
                            end
                        otherwise
                            statestr = '------------------';
                    end
                    subtime = simulation.getSubmissionTime();
                    runstarttime = simulation.getRunStartTime();

                    jobid = simulation.getJobID();
                    if jobid == 0
                        jobidstr = '------';
                    else
                        jobidstr = num2str(jobid);
                    end

                    simulator = simulation.getSimulator();
                    if isempty(simulator)
                        simulatorid = '---';
                    else
                        simulatorid = simulator.getID();
                    end
                    machine = simulation.getMachine();
                    if isempty(machine)
                        machineid = '---';
                    else
                        machineid = machine.getID();
                    end
                    fprintf(f, '%s\n', ['<tr> <td>' id '</td> '...
                                             '<td>' statestr '</td> '...
                                             '<td>' datestr(subtime,'dd-mmm-yyyy HH:MM:SS.FFF') '</td> '...
                                             '<td>' datestr(runstarttime,'dd-mmm-yyyy HH:MM:SS.FFF') '</td> '...
                                             '<td align="right">' jobidstr '</td> '...
                                             '<td>' simulatorid '</td> '...
                                             '<td>' machineid '</td>' ...
                                             '<td>' resultStr '</td></tr>'...
                                             ]); 
                end
                fprintf(f, '%s\n', ['</table>']);
                fprintf(f, '%s\n', ['']); 
            otherwise
                fprintf(f, '%s\n', ['<p>Invalid DisplayStatusWebPage.</p>']); 
        end
        fprintf(f, '%s\n', ['<hr>']); 
        fclose(f);
    
        if usejava('Desktop')
            if obj.showWebPage
                webstr = ['file:///' filepath];
        %        dos(['start ' webstr]);  % This works but is very choppy
                if strcmp(stage, 'initial')
                    com.mathworks.mlservices.MatlabDesktopServices.getDesktop.closeGroup('Web Browser');
                    web(webstr,'-new', '-noaddressbox', '-notoolbar');
                else
                    web(webstr, '-noaddressbox','-notoolbar');
                end
            end
        end 
    end
end 
